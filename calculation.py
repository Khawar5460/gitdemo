# Define two numbers
num1 = 10
num2 = 5

# Perform basic math calculations
addition = num1 + num2
subtraction = num1 - num2
multiplication = num1 * num2
division = num1 / num2
modulus = num1 % num2
exponentiation = num1 ** num2

# Print the results
print(f"Addition of {num1} and {num2} is: {addition}")
print(f"Subtraction of {num1} and {num2} is: {subtraction}")
print(f"Multiplication of {num1} and {num2} is: {multiplication}")
print(f"Division of {num1} by {num2} is: {division}")
print(f"Modulus of {num1} and {num2} is: {modulus}")
print(f"Exponentiation of {num1} to the power of {num2} is: {exponentiation}")
